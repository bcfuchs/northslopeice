	<h3>Kestrel upload instructions</h3>
	<ol>
	  <li>Upload the photo using the uploader below</li>
	  <li>When the uploaded file appears, click on "edit"</li>
	  <li>At the bottom of the edit page, tick "<span style="color:red;">transcribe this kestrel</span>" and save. </li>
	  <li>This setting can be changed in <strong>Dashboard > Media > Library</strong> by clicking on the image and unticking the transcribe box.</li>
	</ol>
